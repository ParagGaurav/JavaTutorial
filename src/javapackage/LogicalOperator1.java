package javapackage;

public class LogicalOperator1 {

	public static void main(String[] args) {
		int a = 10; // local Variable 
		int b = 5; // Local Variable 
		int c = 20; // Local Variable
		
		// Logical Operator 
		
		System.out.println(a<b && a<c);// false
		System.out.println(a); // 10 
		
		// Bitwise Operator
		
		System.out.println(a<b & a<c); // false
		
		

	}

}
